        ];

        $imageUrl = $imageUrls[array_rand($imageUrls)];
        $client = new Client();
        $response = $client->get($imageUrl);
        $imageContents = $response->getBody()->getContents();

        $imageName = Str::random(40) . '.jpg';
        Storage::disk('public')->put('photos/' . $imageName, $imageContents);

        return [
            'classifield_id' => Classifield::factory(),
            'path' => 'photos/' . $imageName,
        ];
    }
}
